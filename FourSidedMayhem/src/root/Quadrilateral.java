package src.root;

public abstract class Quadrilateral {
	private Point point1;
	private Point point2;
	private Point point3;
	private Point point4;
	
	private Line line1;
	private Line line2;
	private Line line3;
	private Line line4;
	
	abstract void parallels();
	abstract double getPerimeter();
	abstract double getArea();
	
}
